#include <stdio.h>
int main(){
    int a=4,b=8;
    //ternary operators are the bool powered operator that can be called shorthand if else statements. Their syntax goes like for example if we want to see if a is greater than b:
    printf(a>b ? "true" : "false");
    //This will return false because a is not greater than b.
    return 0;
}