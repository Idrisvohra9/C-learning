// Arithmetic operators.
#include <stdio.h>
#include <math.h>

int main()
{
   int a, b, c, d;
   a = 2;
   b = 10;
   c = 7;
   d = 3;

   printf("Multiply: %d\n", b * a); // Output=20
   printf("Divide: %d\n", b / a);   // Output=5
   printf("Subtract: %d\n", c - d); // Output=4
   printf("Square of 3: %d\n", d * d);                   // Output=9
   printf("Cube of 3: %f\n", pow(4, 3)); // Output=27.000000
   return 0;
}


